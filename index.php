                        url: 'envia-email-orcamento.php',
                        data: {nome: nome,
                            email: email,
                            telefone: telefone},
                        async: true
                    })
                            .done(function (dados)
                            {
                                $("#resultado-email").empty();
                                $("#resultado-email").append(dados);
